#include "JoystickDriver.c"
int encoder = 0;
int value = 0;

/**
Slame the arm down and give the basket a smack
*/
void releaseArm() {
	motor[chucky] = -60;
	nMotorEncoder[chucky]=0;
	while(value > -190) {
		value = nMotorEncoder[chucky];
	}
}

/**
Raise the arm up and make it hit itself
*/
void raiseArm() {
	motor[chucky] = 60;
	while(value < -50) {
		value = nMotorEncoder[chucky];
	}
}

/**
Drive foward
The speed provided in the parameter will be the amount of power given to the motor
*/
void moveForward(int speed) {
	motor[backRight] = -speed;
	motor[frontRight] = -speed;
	motor[backLeft] = speed;
	motor[frontLeft] = speed;
}

/**
FULL SPEED AHEAD!
Move foward with full power
*/
void moveFast() {
	motor[backRight] = -100;
	motor[frontRight] = -100;
	motor[backLeft] = 100;
	motor[frontLeft] = 100;
}

/**
Rotate the robot. The value provided in the parameter will be multiplied by 50, the result will be used as the amount of
power given to the motor
*/
void moveRotate(int value) {
	motor[backRight] = 50 * value;
	motor[frontRight] = 50 * value;
	motor[backLeft] = 50 * value;
	motor[frontLeft] = 50 * value;
}

/**
Stop moving..
*/
void stopMoving() {
	motor[backRight] = 0;
	motor[frontRight] = 0;
	motor[backLeft] = 0;
	motor[frontLeft] = 0;
}

/**
==== UTILITY METHODS, DO NOT MODIFY ===
*/

int delay = 0;
bool waitForS = true;
void delayOption() {
	int odelay = -1;
	while (true) {
		if (nNxtButtonPressed == 3)
			break;
		else if (nNxtButtonPressed == 1) {
			delay++;
		} else if (nNxtButtonPressed == 2) {
			delay--;
		}
		if (delay < 0)
			delay = 0;
		eraseDisplay();
		nxtDisplayCenteredBigTextLine(3, "%d", delay);
		wait1Msec(1);
	}
}

void askWaitForStart() {
	while (true) {
		if (nNxtButtonPressed == 3)
			break;
		else if (nNxtButtonPressed == 1) {
			waitForS = false;
			break;
		} else if (nNxtButtonPressed == 2) {
			waitForS = true;
			break;
		}
		eraseDisplay();
		nxtDisplayCenteredBigTextLine(3, "(Y)  Wait?  (N)");
		wait1Msec(1);
	}
}

/**
Run the init code for the robot. This will run the Utility methods and activate waitForStart() if
requested by the user
*/
void initRobot() {
	delayOption();
	askWaitForStart();
	if (waitForS) waitForStart();
	wait1Msec(delay);
}


/**
=== MAIN TASK ===
*/
task main()
{
	initRobot(); //DO NOT REMOVE

	nMotorEncoder[frontRight] = 0;
	moveForward(30);
	while (irvalue != 5) {
		irvalue = SensorValue[IR];
	}

	wait1Msec(300);

	stopMoving();

	releaseArm();
	wait1Msec(500);
	raiseArm();

	wait1Msec(1000);
	moveForward(50);
	encoder = nMotorEncoder[frontRight];

	while (encoder > RAMP) encoder = nMotorEncoder[frontRight];

	stopMoving();

	moveRotate(-50);
	wait1Msec(650);
	stopMoving();

	moveForward(50);
	wait1Msec(2500);

	stopMoving();

	moveRotate(50);
	wait1Msec(450);

	stopMoving();

	moveForward(-50);
	wait1Msec(2000);
}
